int	ft_count_if(char **tab, int length, int(*f)(char*))
{
	int	i;
	int	val;

	i = 0;
	val = 0;
	while (i < length)
	{
		if (f(tab[i]) != 0)
			val++;
		i++;
	}
	return (val);
}
