#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		n_lst;
	t_list	*tmp;

	n_lst = 0;
	tmp = lst;
	while (tmp)
	{
		tmp = tmp -> next;
		n_lst++;
	}
	return (n_lst);
}
