#include <unistd.h>
#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	s_len;

	s_len = ft_strlen(s);
	if (s == 0 || fd < 0)
		return ;
	write(fd, s, s_len);
}
